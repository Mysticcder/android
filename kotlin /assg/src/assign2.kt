fun main() {
    var phy: Int= 50
    var math: Int= 60
    var kis: Int= 92
    var bio: Int= 52

    var total = phy + math + kis + bio
    var mean = total/4

    println(mean)


    if(mean >=80){
        println("A")

     }else if(mean >= 60){
          println("B")

     }else if(mean >=40){
          println("C")

     }else{
          println("D")
     }

    var grade = 1
    while (grade < 1000000000000000000){
        println(grade)
        grade++
    }

}